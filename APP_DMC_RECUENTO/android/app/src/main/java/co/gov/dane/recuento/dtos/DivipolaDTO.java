package co.gov.dane.recuento.dtos;

public class    DivipolaDTO {

    private String id;
    private String codigoDepartamento;
    private String codigoMunicipio;
    private String codigoCentroPoblado;
    private String departamento;
    private String municipio;
    private String centroPoblado;
    private String tipoCentroPoblado;
    private String longitud;
    private String latitud;
    private String distrito;
    private String tipoMunicipio;
    private String areaMetropolitana;

    /*Constructor*/
    public DivipolaDTO() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCodigoDepartamento() {
        return codigoDepartamento;
    }

    public void setCodigoDepartamento(String codigoDepartamento) {
        this.codigoDepartamento = codigoDepartamento;
    }

    public String getCodigoMunicipio() {
        return codigoMunicipio;
    }

    public void setCodigoMunicipio(String codigoMunicipio) {
        this.codigoMunicipio = codigoMunicipio;
    }

    public String getCodigoCentroPoblado() {
        return codigoCentroPoblado;
    }

    public void setCodigoCentroPoblado(String codigoCentroPoblado) {
        this.codigoCentroPoblado = codigoCentroPoblado;
    }

    public String getDepartamento() {
        return departamento;
    }

    public void setDepartamento(String departamento) {
        this.departamento = departamento;
    }

    public String getMunicipio() {
        return municipio;
    }

    public void setMunicipio(String municipio) {
        this.municipio = municipio;
    }

    public String getCentroPoblado() {
        return centroPoblado;
    }

    public void setCentroPoblado(String centroPoblado) {
        this.centroPoblado = centroPoblado;
    }

    public String getTipoCentroPoblado() {
        return tipoCentroPoblado;
    }

    public void setTipoCentroPoblado(String tipoCentroPoblado) {
        this.tipoCentroPoblado = tipoCentroPoblado;
    }

    public String getLongitud() {
        return longitud;
    }

    public void setLongitud(String longitud) {
        this.longitud = longitud;
    }

    public String getLatitud() {
        return latitud;
    }

    public void setLatitud(String latitud) {
        this.latitud = latitud;
    }

    public String getDistrito() {
        return distrito;
    }

    public void setDistrito(String distrito) {
        this.distrito = distrito;
    }

    public String getTipoMunicipio() {
        return tipoMunicipio;
    }

    public void setTipoMunicipio(String tipoMunicipio) {
        this.tipoMunicipio = tipoMunicipio;
    }

    public String getAreaMetropolitana() {
        return areaMetropolitana;
    }

    public void setAreaMetropolitana(String areaMetropolitana) {
        this.areaMetropolitana = areaMetropolitana;
    }
}
